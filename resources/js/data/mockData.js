/**
 * Stats helpers
 */
export function getInterviewStats(interviews) {
    return {
        total: interviews.length,
        draft: interviews.filter((i) => i.status === 'draft').length,
        submitted: interviews.filter((i) => i.status === 'submitted').length,
        verified: interviews.filter((i) => i.status === 'verified').length,
        locked: interviews.filter((i) => i.status === 'locked').length,
    };
}

export function getUserStats(users) {
    return {
        total: users.length,
        active: users.filter((u) => u.isActive).length,
        surveyors: users.filter((u) => u.role === 'surveyor').length,
        koordinators: users.filter((u) => u.role === 'koordinator').length,
        admins: users.filter((u) => u.role === 'admin').length,
    };
}
